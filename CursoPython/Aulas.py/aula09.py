#fatiamento de strings

frase = 'Curso em Video Python'

print(frase[9]) #vai imprimir o indice 9 da string armazenada em frase. 


#-------------------------------------------------------------------------------------------------------------------------------------------------------------


frase1 = 'Curso em Video Python'

print(frase1[9:13]) #vai imprimir iniciando do indice 9 até o 13. resultado será: Vide, pois vai puxar 1 indice antes do indicado no final.

#------------------------------------------------------------------------------------------------------------------------------------------------------------

frase2 = 'Curso em Video Python'

print(frase2[9:21]) #vai imprimir iniciando do indice 9 até o 21. resultado sera: Video Python, pois vai puxar 1 indice antes do indicado no final.

#------------------------------------------------------------------------------------------------------------------------------------------------------------

frase3 = 'Curso em Video Python'

print(frase3[9:21:2]) #vai imprimir começando no indice 9 e parar no 21, e o 2 significa que ele vai pular de 2 em 2 na string frase.


#------------------------------------------------------------------------------------------------------------------------------------------------------------


frase4 = 'Curso em Video Python'

print(frase4[:5]) #vai imprimir partindo do indice 0 até o 5. é a mesma coisa que fizesse [0:5]. 

#------------------------------------------------------------------------------------------------------------------------------------------------------------


frase5 = 'Curso em Video Python'

print(frase5[15:]) #é a mesma coisa que [15:21] nessa situação. 

#------------------------------------------------------------------------------------------------------------------------------------------------------------

frase6 = 'Curso em Video Python'

print(frase6[9::3]) #começa no indice 9, vai até o final da string, e vai pular de 3 em 3. 




